public class AssignmentOperator
{  
    public static void main(String []args)
    {
        int n1,n2,n3,n4;
        n1=n2=n3=n4=5;
        System.out.println(n1 + " " +n2 + " " +n3+ " "+n4);

        int a=10;
        a+=5;
        System.out.println(a);

        int b=10;
        b-=5;
        System.out.println(b);

        int c=10;
        c*=3;
        System.out.println(c);

        int d=10;
        d/=5;
        System.out.println(d);

        int e=10;
        e%=6;
        System.out.println(e);
    }

    
}
